	//	AULA 4 - MÉTODOS.
	
//	Um métodos dentro da programação da programação java é responsável pelas aççoes
//	que são realizadas dentro de cada objeto.
	
//	Dentro da linguagem de progamação temos vários nomes para métodos:
//	1 - funções
//	2 - comportamentos
//	Mas no java, esses métodos são responsáveis para que o comportamento se torne uma ação do objeto.
//	Portanto, é no método que definimos as habilidade de um objeto, ou seja, quais as ações que um objeto pode realizar.
	
//	SINTAX PARA DECLARAR UM MÉTODO:
//	1 - incialmente é preciso declarar um <modificador>, ou seja, qual o nível de acessibilidade que esse métodos vai ter.
//	Se vai ser público, privado....
//	2 - na sequência...<modificador><tipo de retorno>...eu pfreciso declarar o tipo de retorno que aquele método vai ter.
//	Tem médotos que não vão retornar nenhuma informação, dai usaremos a palavra reservada "void". Mas se o método retornar
//	alguma informação, tal como o saldo de uma conta (exemplo que está sendo progamado), nos vamos aqui, colocar o tipo de dado
//	a ser retornado, por exemplo um "double".
//	3 - na sequência é atribuido o nome do método, dai ficaria: <modificador><tipo de retorno><nomeDoMetodo>. Ou seja, o nome que
//	faz sentido para aquele comportamento, aquela ação para ser realizado. Exemplo: "ExibirSaldo". Note que é sempre interessante
//	trabalhar com o Camel Model, que nesse caso, "lowerCamelCase" (vamos começar o nome do métodos com letra minúscula e sempre que
//	tiver algum verbo ou algum substantivo, eu coloco em letra maiúscula novamente.
//	4 - logo em seguida, vou abrir parênteses, e vou colocar a lista de argumentos. A expressão toda ficaria da seguinte forma:
//	<modificador><tipo de retorno><nomeDoMetodo>(<[lista de argumentos]>)
//	O que o método precisa de receber de informação para poder fazer a sua lógica? Nesta lista de argumentos eu posso não ter nenhum
//	argumento a ser trabalhado, como posso ter vários argumentos a serem trabalhados. Um exemplo de lista de argumentos:
//	void depositar(int agencia, string numeroConta, double valor)
//	->void depositar é um método depositar não tem um tipo de retorno, portanto ele void e ele também não tem nenhum tipo de modificador
//	é o que chamamos de friendely.
//	->na sequencia...(int agencia, string numeroConta, double valor) isso é uma lsita de argumentos, que também podemos chamar de parâmetro
//	5 - Na sequência, abro uma chaves para poder inicializar o bloco de instruções, ou seja, a lógica, a ação do método a ser seguido. 
//	Ao final do bloco de instruções, eu fecho a chaves concretizando a lógica daquele métodos. 
	
//	<modificador><tipo de retorno><nomeDoMetodo>(<[lista de argumentos]>)
//	{
//	 instruções;
//	}
	
	
	//	AULA 5 - SOBRECARGA DE MÉTODOS.
	
//	No java é possível trabalhar com mais de um comportamento (vulgo método) com o mesmo nome, isso é o que chamamos de SOBRECARGA.
//	A sobrecarga na programação orientada a objetos é reposável por permitir mais de um comportamento para aquela objeto.
//	Assinatura de um método é composta pelo nome do método e os tipo dos dados do parâmetro de entrada desse métodos
//	Eu posso ter métodos, atrbutos e variáveis com o mesmo nome.
//	Para diferencia método, do atributo, da viárvel, utilizamos o termo "this".
//	Duas características diferenciam os métodos com o mesmo nome: o NÚMERO DE PARÊMETRO e o TIPO delas. Essas características fazem 
//	parte da ASSINATURA DE UM MÉTODOS.
	
	
	//	AULA 6 - CONSTRUTORES
	
//	É possível criar lógica durante a construção de objetos, daí o nome CONSTRUTORES. Dentro de cada classe, podemos gerar 1 ou vários
//	construtores, para que no ato de criação dos objetos, possamos escolher qual o tipo de construtor a ser usado.
//	O construtor precisa ter o mesmo nome de uma classe. Além disso, ele não terá nenhum tipo de retorno, nem mesmo o void e também não 
//	não aceita nenhum comando return.
//	O construtor é acionado quando realizado o comando new. Então aquele objeto é alocado em memória, todos os atributos são inicializados
//	daquele objeto e logo em seguida que o construtor é acionado. Toda classe é preciso ter no mínimo um construtor.
	
	
